#include "TimeToFrame.h"



//================================================================================
//
// [ フレーム数→時間に変換関数 ]
//
//================================================================================

void Frame_To_Time(int frame, float* hour, float* min, float* sec)
{
	*hour = Frame_To_Hour  (frame);
	*min  = Frame_To_Minute(frame % Hour_To_Frame  (1));
	*sec  = Frame_To_Second(frame % Minute_To_Frame(1));
}