#ifndef	_TIME_TO_FRAME_H_
#define _TIME_TO_FRAME_H_



//**********************************************************************
//
// マクロ定義
//
//**********************************************************************

const unsigned FPS = 60;



//**********************************************************************
//
// インライン関数宣言
//
//**********************************************************************

//================================================================================
//
// [ 秒→フレーム数変換関数 ]
//
//================================================================================

inline int Second_To_Frame(float sec)
{
	return (int)(sec * FPS);
}



//================================================================================
//
// [ 分→フレーム数変換関数 ]
//
//================================================================================

inline int Minute_To_Frame(float min)
{
	const float tempSec = min * 60;

	return (int)(Second_To_Frame(tempSec));
}



//================================================================================
//
// [ 時→フレーム数変換関数 ]
//
//================================================================================

inline int Hour_To_Frame(float hour)
{
	const float tempMin = hour * 60;

	return (int)(Minute_To_Frame(tempMin));
}



//================================================================================
//
// [ フレーム数→秒変換関数 ]
//
//================================================================================

inline float Frame_To_Second(int frame)
{
	return (float)frame / Second_To_Frame(1.0f);
}



//================================================================================
//
// [ フレーム数→分変換関数 ]
//
//================================================================================

inline float Frame_To_Minute(int frame)
{
	return (float)frame / Minute_To_Frame(1.0f);
}



//================================================================================
//
// [ フレーム数→時変換関数 ]
//
//================================================================================

inline float Frame_To_Hour(int frame)
{
	return (float)frame / Hour_To_Frame(1.0f);
}






//**********************************************************************
//
// プロトタイプ宣言
//
//**********************************************************************

//================================================================================
//
// [ フレーム数→時間に変換関数 ]
//
//================================================================================

void Frame_To_Time(int frame, float* hour, float* min, float* sec);






#endif